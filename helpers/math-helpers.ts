export function addNumbers (num1:number, num2:number){
    return num1 + num2;
}

export function substractNumbers (num1:number, num2:number){
    return num1 - num2;
}

export function timesNumbers (num1:number, num2:number){
    return num1 * num2;
}

export function divideNumbers (num1:number, num2:number){
    return num1 / num2;
}